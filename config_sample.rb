module Settings
    @CONSUMER_KEY = ""
    @CONSUMER_SECRET = ""
    @ACCESS_TOKEN = ""
    @ACCESS_SECRET = ""

    def self.CONSUMER_KEY
        return @CONSUMER_KEY
    end

    def self.CONSUMER_SECRET
        return @CONSUMER_SECRET
    end

    def self.ACCESS_TOKEN
        return @ACCESS_TOKEN
    end

    def self.ACCESS_SECRET
        return @ACCESS_SECRET
    end
end
